using APIK8S.Helpers;
using APIK8S.Models;
using APIK8S.Services;
using Microsoft.Extensions.DependencyInjection;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();

builder.Services.Configure<ApplicationOptions>(builder.Configuration.GetSection("ApplicationOptions"));
builder.Services.Configure<QuoteStoreDatabaseSettings>(builder.Configuration.GetSection("QuoteStoreDatabase"));

builder.Services.AddSingleton<QuoteService>();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
